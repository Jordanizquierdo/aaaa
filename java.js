function oscuro(){
    let CambiarColor = document.body.style.backgroundColor
    body.style.backgroundColor = CambiarColor === 'black' ? 'white' : 'black';
    let cambiarletra = document.body.style.color
    body.style.color = cambiarletra === 'white' ? 'black' : 'white'


}
function tamanio(){
   let ta = document.body.style.fontSize 
   body.style.fontSize = ta === "100%" ? "150%" : "100%"

}